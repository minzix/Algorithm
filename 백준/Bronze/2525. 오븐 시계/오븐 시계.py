# 1. 입력 받기
A, B = map(int, input().split())  # 현재 시각
C = int(input())  # 오븐구이에 필요한 시간 (분 단위)

# 2. 시간과 분을 분으로 변환하여 새로운 변수로 저장
current_time_in_minutes = A * 60 + B

# 3. 시각에 오븐구이에 필요한 시간을 더하고, 24 이상이면 24를 빼주어 0부터 23 사이의 시각으로 변환
end_time_in_minutes = (current_time_in_minutes + C) % (24 * 60)

# 4. 최종 결과 출력
end_hour = end_time_in_minutes // 60
end_minute = end_time_in_minutes % 60
print(f"{end_hour} {end_minute}")    

# < 문제 풀이 >
# 1. 단위를 분으로 다 통일함
# 2. 24시간을 넘어가는 경우를 처리하기 위해 if문을 사용하는 대신, %(나머지) 구하기를 활용해서 
#    전체 분을 24시간에 60을 곱한 수(하루의 전체 분)으로 나눠서 그 나머지를 취하는 방식 사용
#    만약 24시간보다 작으면 그 수가 그대로 나머지임
#    24시간보다 큰 경우, 그 수에서 24를 뺀 수가 나머지임
# 3. 최종 결과 출력 시, 시간은 60으로 나눈 몫이고 분은 60으로 나눈 나머지임
